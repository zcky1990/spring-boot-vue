package app.config;

public class CloudinaryEnv {
	String env;
	CloudinaryConfig config;
	
	public String getEnv() {
		return env;
	}
	public void setEnv(String env) {
		this.env = env;
	}
	public CloudinaryConfig getConfig() {
		return config;
	}
	public void setConfig(CloudinaryConfig config) {
		this.config = config;
	}
}
